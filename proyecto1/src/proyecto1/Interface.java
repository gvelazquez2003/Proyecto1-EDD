    private void loadFile(String filename){
        dict = new Dictionary();
        board = new Board();
        
        try {
            File in = new File(filename);
            Scanner lector = new Scanner(in);
            String linea = lector.nextLine();
            if (linea.equals("dic")){
                linea = lector.nextLine();
                while (!linea.equals("/dic") ) { 
                    dict.addWord(linea);
                    linea = lector.nextLine();
                }
            }
            linea = lector.nextLine();
            if (linea.equals("tab")){
                linea = lector.nextLine();
                board.builtBoard(linea);
            }

            lector.close();
        } catch (FileNotFoundException e) {
            System.out.println("Archivo Inv√°lido");
        }
    }
    
    private void updateBoard(){
        Node<Letter> current = board.letters.head;
        JTextField[] tiles = {t1, t2, t3, t4, t5, t6, t7, t8, t9, t10, t11, t12, t13, t14, t15, t16};
        for (int i=0; i < 16; i++) {
            if(current.data.visited){
                tiles[i].setBackground(Color.LIGHT_GRAY);
            } else {
                tiles[i].setBackground(Color.WHITE);
            }
            tiles[i].setText(String.valueOf(current.data.character));
            current = current.next;
        }
    }
    
    private void updateDict(){
        Node<Word> current = dict.words.head;
        String text = "";
        while(current != null){
            String word = current.data.word;
            text += word + "\n";
            current = current.next;
        }
        wordList.setText(text);
    }

